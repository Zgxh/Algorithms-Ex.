class Solution {

    /**
     * 思路1：dp
     *
     * @param nums
     * @return
     */
    public int lengthOfLIS(int[] nums) {
        if (nums == null || nums.length == 0) {
            return 0;
        }
        int len = nums.length;
        int[] dp = new int[len]; // 以nums[i]结尾的最长递增子序列的长度
        dp[0] = 1;
        int maxLength = 1;
        for (int i = 0; i < len; i++) {
            // 遍历前面的dp[]，找到序列尾比nums[i]小的所有序列中，长度最大的序列
            for (int j = 0; j < i; j++) {
                if (nums[j] < nums[i]) {
                    dp[i] = Math.max(dp[i], dp[j] + 1);
                }
            }
            maxLength = Math.max(maxLength, dp[i]);
        }

        return maxLength;
    }


}
