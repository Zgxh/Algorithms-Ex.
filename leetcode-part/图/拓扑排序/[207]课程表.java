class Solution {

    // æ‹“æ‰‘æ’åºï¼Œå¦‚æœå‘ç°äº†å¾ªç¯ä¾èµ–ï¼Œåˆ™è¿”å›false

    int[] status; // æ¯ä¸ªç»“ç‚¹çš„è®¿é—®çŠ¶æ€ï¼Œ0æœªè®¿é—®ï¼Œ1è®¿é—®ä¸­ï¼Œ2è®¿é—®ç»“æŸ
    Map<Integer, List<Integer>> map;

    public boolean canFinish(int numCourses, int[][] prerequisites) {
        status = new int[numCourses];
        map = new HashMap();
        for (int[] prerequisite : prerequisites) {
            List<Integer> list = new ArrayList();
            if (map.containsKey(prerequisite[0])) {
                list = map.get(prerequisite[0]);
            }
            list.add(prerequisite[1]);
            map.put(prerequisite[0], list);
        }
        for (int course : map.keySet()) {
            if (canFinishHelp(course) == false) {
                return false;
            }
        }

        return true;
    }

    private boolean canFinishHelp(int course) {
        if (status[course] == 2) {
            return true;
        } else if (status[course] == 1) { // å‘ç°äº†å¾ªç¯ä¾èµ–
            return false;
        }
        if (!map.containsKey(course)) {
            status[course] = 2;
            return true;
        }
        status[course] = 1;
        for (int pre : map.get(course)) {
            if (canFinishHelp(pre) == false) {
                return false;
            }
        }
        status[course] = 2;

        return true;
    }
}
